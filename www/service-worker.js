// Service worker mínimo de Zocial — solo lo necesario para que la app sea
// instalable como PWA (Safari/iOS y Chrome/Android lo exigen). No cachea
// agresivamente para no mostrar contenido viejo de la red social por error.
const CACHE_NAME = 'zocial-shell-v1';
const SHELL_FILES = ['./index.html', './manifest.json'];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(SHELL_FILES))
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))
    )
  );
  self.clients.claim();
});

// estrategia "red primero, respaldo en caché" — así siempre se ve lo más
// reciente cuando hay internet, y no se rompe del todo si se pierde la señal
self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') return;
  event.respondWith(
    fetch(event.request)
      .then((response) => {
        const copy = response.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
        return response;
      })
      .catch(() => caches.match(event.request))
  );
});
